    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {                                      
        try {
            // removendo a pessoa selecionada da nossa lista
        
        listaPessoas.remove(jComboBox1.getSelectedIndex());
        // removendo ela da combobox
        jComboBox1.removeItemAt(jComboBox1.getSelectedIndex());
        } catch (IndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(null, "Amigue, voce tentou deletar um item que nao existe!","Erro!",JOptionPane.ERROR_MESSAGE);
        }
        
    }                                     

