    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {                                      
        
            try {
            // pegando os valores dos campos
            String nome = campoNome.getText();
            int idade =  Integer.parseInt(campoIdade.getText());
            String cpf = campoCPF.getText();
            Endereco endereco = new Endereco(campoEstado.getText(),campoCidade.getText(),campoBairro.getText(),campoRua.getText(),campoCEP.getText(),campoTelefone.getText());
            // atribuindo pessoa a lista de pessoas
           
            listaPessoas.add(new Pessoa(nome,idade,cpf,endereco));
            
            System.out.println(listaPessoas.toArray());
            jComboBox1.addItem(nome);
            //limpando os campos de texto apos inserir u pessoe
            campoBairro.setText("");
            campoCEP.setText("");
            campoCPF.setText("");
            campoCidade.setText("");
            campoEstado.setText("");
            campoIdade.setText("");
            campoNome.setText("");
            campoRua.setText("");
            campoTelefone.setText("");
            
        } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Por favor insira os valores corretamente!","Erro!",JOptionPane.ERROR_MESSAGE);
        }
            

                    
          


           
        
        
        // TODO add your handling code here:
    }                                     

